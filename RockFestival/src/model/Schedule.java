package model;

public class Schedule {
	private String band;
	private String scene;
	private String time;
	
	public Schedule(String band, String scene, String time) {
		this.band = band;
		this.scene = scene;
		this.time = time;
	}

	public String getBand() {
		return band;
	}

	public void setBand(String band) {
		this.band = band;
	}

	public String getScene() {
		return scene;
	}

	public void setScene(String scene) {
		this.scene = scene;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}
	
	
	

}
